#include <stdio.h>
int main() {
    int i;
    int n;
    int a[n];
    int count=1;
    scanf("%d",&n);
    for(i=1;i<=n;i=i+1) {
        scanf("%d",&a[n]);
        int d;
        d=a[n];
        if(a[n]==d) {
            count=count+1; } }
    if(count==d) {
        printf("Yes"); }
    else {
        printf("No"); }
    return 0; }