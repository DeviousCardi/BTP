#include <stdio.h>
int main(){
    int n;
    scanf("%d",&n);
    int a[n];
    int b[n];
    for(int i=0;i<n&&i<=100;i++) {
        scanf("%d",&a[i]); }
    for(int i=0;i<n&&i<=100;i++) {
        scanf("%d",&b[i]); }
    for(int i=b[i];i<n&&i<=100;i++) {
        scanf("%d",&b[i]); }
    printf("end");
    return 0; }