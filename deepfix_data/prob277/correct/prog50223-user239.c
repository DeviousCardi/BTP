#include <stdio.h>
#include <stdlib.h>
int main() {      int n;
                     scanf("%d",&n);
    	              int i,j,k;
    	              for(i=-n;i<=n;i++)
    	              {   k=i;j=n;
    	                  if(k = 0,j--)
    	                  {if  (j<=k)
    	                    printf("*");
    	                    else
    	                    printf(" "); }
    	                  printf("\n"); }
	return 0; }