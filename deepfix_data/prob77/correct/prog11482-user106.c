#include <stdio.h>
int main() {
    int i=0,n,num[1000],max,min,p,b;
    scanf("%d",&n);
    int a[i];
    for(i=0;i<n;i++) {
        scanf("%d",&num[i]);
        if(max<a[i])
            max=i;
        else if(min>a[i])
            min=i; }
      max=p;
      b=min;
      p=b;
      printf("%d",a[i]);
    printf("end");
    return 0; }