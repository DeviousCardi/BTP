#include <stdio.h>
#include <stdlib.h>
int main() {
	                int i,n;
	                int k=0;
	                scanf("%d",&n);
	                for(i=1;i<=n;++i)
	               { while(k!=2*i-1)
	                   { printf("%d",n);
	                 }  ++k; }
	                k=0;
	                printf("\n");
	return 0; }