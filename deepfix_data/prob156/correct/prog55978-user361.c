#include <stdio.h>
int main() {
    int n,m,i,j,max=0,sum=0;
    int a;
    scanf("%d %d",&n,&m);
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            scanf("%d",&a);
}}
for(i=0;i<n;i++){
        for(j=0;j<m;j++) {
            sum=sum+a;}
            if(sum>max){
                max=sum;
            }}
    printf("%d",max);
    return 0; }