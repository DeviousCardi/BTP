#include <stdio.h>
#include <stdlib.h>
int main() {
	               double  a,b,x;
	               int     q,w,s,ar,i,n;
	               scanf("%lf%lf%d",&a,&b,&n);
	               ar=0;
	               for (i=0;i<=n;i++){
	                   x=(b-a)/n;
	                   s= ((q*i)-(w*i))*x;
	                   ar= ar + s; }
	return 0; }